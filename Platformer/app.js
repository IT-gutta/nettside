function Rect(x, y, w, h){
  this.pos = createVector(x, y),
  this.vel = createVector(),
  this.acc = createVector(0, 1),
  this.w = w,
  this.h = h,
  this.jumping = false,
  this.color = "grey",
  this.sx = 200,
  this.sy = 0,
  this.sprite = playerSprite,


  this.sides =  {
    top : this.pos.y,
    bottom : this.pos.y+this.h,
    left : this.pos.x+25,
    right : this.pos.x+this.w-25
 }



  this.draw = function(){
    image(this.sprite, this.pos.x, this.pos.y, 80, 80, this.sx, this.sy, 180, 180)
    // fill(this.color)
   // rect(this.pos.x, this.pos.y, this.w, this.h)
  }



  this.update = function(){
    this.pos.x+=this.vel.x;
    this.pos.y+=this.vel.y;
    this.vel.x+=this.acc.x;
    this.vel.y+=this.acc.y;



  this.beforeTop = this.sides.top;
  this.beforeBottom = this.sides.bottom;
  this.beforeLeft = this.sides.left;
  this.beforeRight = this.sides.right;




    this.sides =  {
    top : this.pos.y,
    bottom : this.pos.y+this.h,
    left : this.pos.x+25,
    right : this.pos.x+this.w-25
 };










    if(keyIsDown(RIGHT_ARROW)){
      this.vel.x=5; this.sy = 0}
    else if(keyIsDown(LEFT_ARROW)){
      this.vel.x=-5; this.sy = 200}
   else{this.vel.x*=0.9}


    if(keyIsDown(32) && this.jumping==false){jump(this, 18)}

    if(keyIsDown(DOWN_ARROW) && this.jumping==false){this.h*=0.5}
    else{this.h=h}



    if(this.pos.y+this.h> 28*tileSize-128){this.vel.y=0;
      this.pos.y= 28*tileSize-this.h-128; this.jumping=false }








  this.draw();
 }
}



function Block(x, y, w, h, bool, f){
  this.x = x,
  this.y = y,
  this.w = w,
  this.h = h,
  this.f = f,


  this.sides =  {
    top : this.y,
    bottom : this.y+this.h,
    left : this.x,
    right : this.x+this.w
 }


if(bool){
this.draw = function(){
   image(this.f, this.x, this.y, 32, 32)

  }
}

else{
  this.draw = function(){
    noStroke()
    fill(this.f)
    rect(this.x, this.y, this.w, this.h);
  }

}

}


function isColliding(rect1, rect2){
  if(rect1.sides.right<rect2.sides.left || rect1.sides.left>rect2.sides.right || rect1.sides.bottom<rect2.sides.top || rect1.sides.top>rect2.sides.bottom){return false}

  return true;
}




